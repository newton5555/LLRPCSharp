using System.Globalization;
using LlrpNet.ProtocolGenerator.Generation;
using LlrpNet.ProtocolModel.Definitions;

namespace LlrpNet.ProtocolGenerator.Internal;

internal sealed partial class ProtocolSourceRenderer
{
    private const string Header = "// <auto-generated />";

    private readonly string rootNamespace;
    private readonly string versionNamespace;
    private readonly ProtocolSymbolTable symbols;
    private readonly IReadOnlyDictionary<string, IReadOnlyList<string>> parameterChoices;
    private readonly IReadOnlyDictionary<string, ChoiceDefinition> choiceDefinitions;
    private readonly IReadOnlySet<string> choicesContainingEnvelope;
    private readonly IReadOnlySet<string> canonicalTrailingEnvelopeChoices;

    public ProtocolSourceRenderer(
        string rootNamespace,
        string versionNamespace,
        ProtocolSymbolTable symbols,
        IReadOnlyList<ProtocolDefinition> visibleDefinitions)
    {
        this.rootNamespace = rootNamespace;
        this.versionNamespace = versionNamespace;
        this.symbols = symbols;
        parameterChoices = BuildParameterChoices(visibleDefinitions);
        choiceDefinitions = visibleDefinitions
            .SelectMany(static definition => definition.Choices)
            .GroupBy(static choice => choice.Name, StringComparer.Ordinal)
            .ToDictionary(static group => group.Key, static group => group.First(), StringComparer.Ordinal);
        choicesContainingEnvelope = choiceDefinitions.Values
            .Where(choice => choice.ParameterTypes.Any(symbols.IsEnvelopeParameter))
            .Select(static choice => choice.Name)
            .ToHashSet(StringComparer.Ordinal);
        canonicalTrailingEnvelopeChoices = FindCanonicalTrailingEnvelopeChoices(visibleDefinitions);
    }

    public IReadOnlyList<GeneratedSourceFile> Render(ProtocolDefinition definition)
    {
        var sources = new List<GeneratedSourceFile>();
        int index = 0;
        foreach (EnumerationDefinition enumeration in definition.Enumerations)
        {
            sources.Add(RenderEnumeration(
                enumeration.Name,
                enumeration.Entries,
                index,
                isCustom: false));
            index++;
        }

        foreach (CustomEnumerationDefinition enumeration in definition.CustomEnumerations)
        {
            sources.Add(RenderEnumeration(
                enumeration.Name,
                enumeration.Entries,
                index,
                isCustom: true));
            index++;
        }

        index = 0;
        foreach (ChoiceDefinition choice in definition.Choices)
        {
            sources.Add(RenderChoice(choice, index));
            index++;
        }

        index = 0;
        foreach (MessageDefinition message in definition.Messages)
        {
            sources.Add(RenderMessage(message, index));
            index++;
        }

        foreach (CustomMessageDefinition message in definition.CustomMessages)
        {
            sources.Add(RenderCustomMessage(message, index));
            index++;
        }

        index = 0;
        foreach (ParameterDefinition parameter in definition.Parameters)
        {
            sources.Add(RenderParameter(parameter, index));
            index++;
        }

        foreach (CustomParameterDefinition parameter in definition.CustomParameters)
        {
            sources.Add(RenderCustomParameter(parameter, index));
            index++;
        }

        return sources;
    }

    private GeneratedSourceFile RenderEnumeration(
        string sourceName,
        IReadOnlyList<EnumerationEntryDefinition> entries,
        int index,
        bool isCustom)
    {
        string identifier = symbols.GetEnumeration(sourceName);
        var writer = CreateWriter("Enumerations");
        writer.WriteLine($"/// <summary>Generated LLRP{(isCustom ? " custom" : string.Empty)} enumeration '{EscapeXml(sourceName)}'.</summary>");
        writer.WriteLine($"public enum {identifier} : long");
        writer.WriteLine("{");
        using (writer.Indent())
        {
            var allocator = new CSharpIdentifierAllocator();
            foreach (EnumerationEntryDefinition entry in entries)
            {
                string entryIdentifier = allocator.Allocate(entry.Name, "Value");
                writer.WriteLine($"{entryIdentifier} = {FormatInt64(entry.Value)},");
            }
        }

        writer.WriteLine("}");
        return CreateSource("Enumerations", index, identifier, GeneratedSourceKind.Enumeration, writer);
    }

    private GeneratedSourceFile RenderChoice(ChoiceDefinition choice, int index)
    {
        string identifier = symbols.GetChoice(choice.Name);
        var writer = CreateWriter("Choices");
        writer.WriteLine($"/// <summary>Marks a parameter accepted by the LLRP choice '{EscapeXml(choice.Name)}'.</summary>");
        writer.WriteLine(
            $"public interface {identifier} : global::LlrpNet.Protocol.Parameters.ILlrpParameter");
        writer.WriteLine("{");
        writer.WriteLine("}");
        return CreateSource("Choices", index, identifier, GeneratedSourceKind.Choice, writer);
    }

    private GeneratedSourceFile RenderMessage(MessageDefinition message, int index)
    {
        string identifier = symbols.GetMessage(message.Name);
        var allocator = new CSharpIdentifierAllocator(
            [identifier, "MessageId", "TypeNumber", "MessageType"]);
        List<GeneratedProperty> properties =
        [
            new GeneratedProperty("uint", "MessageId", "The common LLRP message identifier.", Definition: null),
            .. CreateProperties(message.Members, allocator),
        ];
        var writer = CreateWriter("Messages");
        WriteRecordDeclaration(
            writer,
            identifier,
            properties,
            ["global::LlrpNet.Protocol.Messages.ILlrpMessage"],
            $"Generated LLRP message '{EscapeXml(message.Name)}'.");
        using (writer.Indent())
        {
            writer.WriteLine($"public const ushort TypeNumber = {message.TypeNumber.ToString(CultureInfo.InvariantCulture)};");
            writer.WriteLine("public const ushort MessageType = TypeNumber;");
        }

        writer.WriteLine("}");
        return CreateSource("Messages", index, identifier, GeneratedSourceKind.Message, writer);
    }

    private GeneratedSourceFile RenderCustomMessage(CustomMessageDefinition message, int index)
    {
        string identifier = symbols.GetMessage(message.Name);
        var allocator = new CSharpIdentifierAllocator(
            [identifier, "MessageId", "TypeNumber", "MessageType", "VendorIdentifier", "Subtype"]);
        List<GeneratedProperty> properties =
        [
            new GeneratedProperty("uint", "MessageId", "The common LLRP message identifier.", Definition: null),
            .. CreateProperties(message.Members, allocator),
        ];
        var writer = CreateWriter("Messages");
        WriteRecordDeclaration(
            writer,
            identifier,
            properties,
            ["global::LlrpNet.Protocol.Messages.ILlrpMessage"],
            $"Generated custom LLRP message '{EscapeXml(message.Name)}'.");
        using (writer.Indent())
        {
            writer.WriteLine("public const ushort TypeNumber = 1023;");
            writer.WriteLine("public const ushort MessageType = TypeNumber;");
            writer.WriteLine($"public const uint VendorIdentifier = {symbols.GetVendorId(message.Vendor).ToString(CultureInfo.InvariantCulture)}U;");
            writer.WriteLine($"public const byte Subtype = {message.Subtype.ToString(CultureInfo.InvariantCulture)};");
        }

        writer.WriteLine("}");
        return CreateSource("Messages", index, identifier, GeneratedSourceKind.Message, writer);
    }

    private GeneratedSourceFile RenderParameter(ParameterDefinition parameter, int index)
    {
        string identifier = symbols.GetParameter(parameter.Name);
        var allocator = new CSharpIdentifierAllocator([identifier, "TypeNumber", "ParameterType"]);
        List<GeneratedProperty> properties = CreateProperties(parameter.Members, allocator);
        List<string> interfaces = GetParameterInterfaces(parameter.Name);
        var writer = CreateWriter("Parameters");
        WriteRecordDeclaration(
            writer,
            identifier,
            properties,
            interfaces,
            $"Generated LLRP {parameter.Encoding.ToString().ToUpperInvariant()} parameter '{EscapeXml(parameter.Name)}'.");
        using (writer.Indent())
        {
            writer.WriteLine($"public const ushort TypeNumber = {parameter.TypeNumber.ToString(CultureInfo.InvariantCulture)};");
            if (!properties.Any(p => p.Name.StartsWith("ParameterType", StringComparison.Ordinal)))
            {
                writer.WriteLine($"public const ushort ParameterType = {parameter.TypeNumber.ToString(CultureInfo.InvariantCulture)};");
            }
        }

        writer.WriteLine("}");
        return CreateSource("Parameters", index, identifier, GeneratedSourceKind.Parameter, writer);
    }

    private GeneratedSourceFile RenderCustomParameter(CustomParameterDefinition parameter, int index)
    {
        string identifier = symbols.GetParameter(parameter.Name);
        var allocator = new CSharpIdentifierAllocator([identifier, "TypeNumber", "ParameterType", "VendorIdentifier", "Subtype"]);
        List<GeneratedProperty> properties = CreateProperties(parameter.Members, allocator);
        List<string> interfaces = GetParameterInterfaces(parameter.Name);
        foreach (AllowedInDefinition allowed in parameter.AllowedIn)
        {
            if (symbols.TryGetChoice(allowed.Type, out string choiceIdentifier))
            {
                string choiceType = QualifyChoice(allowed.Type);
                if (!interfaces.Contains(choiceType, StringComparer.Ordinal))
                {
                    interfaces.Add(choiceType);
                }
            }
        }

        var writer = CreateWriter("Parameters");
        WriteRecordDeclaration(
            writer,
            identifier,
            properties,
            interfaces,
            $"Generated custom LLRP parameter '{EscapeXml(parameter.Name)}'.");
        using (writer.Indent())
        {
            writer.WriteLine("public const ushort TypeNumber = 1023;");
            writer.WriteLine($"public const uint VendorIdentifier = {symbols.GetVendorId(parameter.Vendor).ToString(CultureInfo.InvariantCulture)}U;");
            writer.WriteLine($"public const uint Subtype = {parameter.Subtype.ToString(CultureInfo.InvariantCulture)}U;");
        }

        writer.WriteLine("}");
        return CreateSource("Parameters", index, identifier, GeneratedSourceKind.Parameter, writer);
    }

    private CodeWriter CreateWriter(string category)
    {
        var writer = new CodeWriter();
        writer.WriteLine(Header);
        writer.WriteLine("#nullable enable");
        writer.WriteLine();
        writer.WriteLine($"namespace {rootNamespace}.{category}.{versionNamespace};");
        writer.WriteLine();
        return writer;
    }

    private GeneratedSourceFile CreateSource(
        string category,
        int index,
        string identifier,
        GeneratedSourceKind kind,
        CodeWriter writer)
    {
        string fileIdentifier = CSharpIdentifier.WithoutEscapePrefix(identifier);
        string hintName = $"{category}/{versionNamespace}/{index.ToString("D4", CultureInfo.InvariantCulture)}.{fileIdentifier}.g.cs";
        return new GeneratedSourceFile(hintName, kind, writer.ToString());
    }

    private List<GeneratedProperty> CreateProperties(
        IReadOnlyList<ProtocolMemberDefinition> members,
        CSharpIdentifierAllocator allocator)
    {
        var properties = new List<GeneratedProperty>();
        foreach (ProtocolMemberDefinition member in members)
        {
            switch (member)
            {
                case FieldDefinition field:
                    properties.Add(new GeneratedProperty(
                        GetFieldType(field),
                        allocator.Allocate(field.Name, "Field"),
                        $"Wire field '{EscapeXml(field.Name)}' ({field.FieldType}).",
                        field));
                    break;

                case ReservedBitsDefinition:
                    break;

                case ParameterReferenceDefinition parameter:
                    string parameterType = symbols.IsEnvelopeParameter(parameter.ParameterType)
                        ? "global::LlrpNet.Protocol.Parameters.ILlrpParameter"
                        : QualifyParameter(parameter.ParameterType);
                    properties.Add(CreateReferenceProperty(
                        parameterType,
                        parameter.ParameterType,
                        parameter.Cardinality,
                        allocator,
                        "parameter"));
                    break;

                case ChoiceReferenceDefinition choice:
                    string choiceType = choicesContainingEnvelope.Contains(choice.ChoiceType)
                        ? "global::LlrpNet.Protocol.Parameters.ILlrpParameter"
                        : QualifyChoice(choice.ChoiceType);
                    properties.Add(CreateReferenceProperty(
                        choiceType,
                        choice.ChoiceType,
                        choice.Cardinality,
                        allocator,
                        "choice"));
                    break;
            }
        }

        return properties;
    }

    private static GeneratedProperty CreateReferenceProperty(
        string elementType,
        string sourceName,
        Cardinality cardinality,
        CSharpIdentifierAllocator allocator,
        string referenceKind)
    {
        bool repeated = cardinality.Maximum is null or > 1;
        string propertySourceName = repeated ? $"{sourceName}Items" : sourceName;
        string propertyName = allocator.Allocate(propertySourceName, "Member");
        string propertyType = repeated
            ? $"global::System.Collections.Generic.IReadOnlyList<{elementType}>"
            : cardinality.Minimum == 0
                ? $"{elementType}?"
                : elementType;
        string description = $"LLRP {referenceKind} '{EscapeXml(sourceName)}' with cardinality {FormatCardinality(cardinality)}.";
        ProtocolMemberDefinition definition = referenceKind == "parameter"
            ? new ParameterReferenceDefinition(sourceName, cardinality)
            : new ChoiceReferenceDefinition(sourceName, cardinality);
        return new GeneratedProperty(propertyType, propertyName, description, definition);
    }

    private string GetFieldType(FieldDefinition field)
    {
        if (field.Enumeration is not null)
        {
            string enumerationType = QualifyEnumeration(field.Enumeration);
            return field.FieldType is ProtocolFieldType.U1Vector
                or ProtocolFieldType.U8Vector
                or ProtocolFieldType.U16Vector
                or ProtocolFieldType.U32Vector
                ? $"global::System.Collections.Generic.IReadOnlyList<{enumerationType}>"
                : enumerationType;
        }

        return field.FieldType switch
        {
            ProtocolFieldType.U1 => "bool",
            ProtocolFieldType.U2 => "byte",
            ProtocolFieldType.S8 => "sbyte",
            ProtocolFieldType.U8 => "byte",
            ProtocolFieldType.S16 => "short",
            ProtocolFieldType.U16 => "ushort",
            ProtocolFieldType.S32 => "int",
            ProtocolFieldType.U32 => "uint",
            ProtocolFieldType.U64 => "ulong",
            ProtocolFieldType.U96 => "global::System.ReadOnlyMemory<byte>",
            ProtocolFieldType.BytesToEnd => "global::System.ReadOnlyMemory<byte>",
            ProtocolFieldType.U1Vector => "global::System.Collections.Generic.IReadOnlyList<bool>",
            ProtocolFieldType.U8Vector => "global::System.ReadOnlyMemory<byte>",
            ProtocolFieldType.U16Vector => "global::System.Collections.Generic.IReadOnlyList<ushort>",
            ProtocolFieldType.U32Vector => "global::System.Collections.Generic.IReadOnlyList<uint>",
            ProtocolFieldType.Utf8Vector => "string",
            _ => throw new InvalidOperationException($"Unsupported field type '{field.FieldType}'."),
        };
    }

    private List<string> GetParameterInterfaces(string parameterName)
    {
        var interfaces = new List<string>
        {
            "global::LlrpNet.Protocol.Parameters.ILlrpParameter",
        };
        if (parameterChoices.TryGetValue(parameterName, out IReadOnlyList<string>? choices))
        {
            interfaces.AddRange(choices.Select(QualifyChoice));
        }

        return interfaces;
    }

    private static void WriteRecordDeclaration(
        CodeWriter writer,
        string identifier,
        IReadOnlyList<GeneratedProperty> properties,
        IReadOnlyList<string> interfaces,
        string summary)
    {
        writer.WriteLine($"/// <summary>{summary}</summary>");
        foreach (GeneratedProperty property in properties)
        {
            writer.WriteLine(
                $"/// <param name=\"{CSharpIdentifier.WithoutEscapePrefix(property.Name)}\">{property.Description}</param>");
        }

        if (properties.Count == 0)
        {
            writer.WriteLine($"public sealed record {identifier} : {string.Join(", ", interfaces)}");
        }
        else
        {
            writer.WriteLine($"public sealed record {identifier}(");
            using (writer.Indent())
            {
                for (int index = 0; index < properties.Count; index++)
                {
                    GeneratedProperty property = properties[index];
                    string suffix = index == properties.Count - 1 ? string.Empty : ",";
                    writer.WriteLine($"{property.Type} {property.Name}{suffix}");
                }
            }

            writer.WriteLine($") : {string.Join(", ", interfaces)}");
        }

        writer.WriteLine("{");
    }

    private string QualifyParameter(string name) =>
        Qualify(symbols.GetParameterRootNamespace(name), "Parameters", symbols.GetParameter(name));

    private string QualifyChoice(string name) =>
        Qualify(symbols.GetChoiceRootNamespace(name), "Choices", symbols.GetChoice(name));

    private string QualifyEnumeration(string name) =>
        Qualify(symbols.GetEnumerationRootNamespace(name), "Enumerations", symbols.GetEnumeration(name));

    private string Qualify(string category, string identifier) =>
        Qualify(rootNamespace, category, identifier);

    private string Qualify(string referenceRootNamespace, string category, string identifier)
    {
        return $"global::{referenceRootNamespace}.{category}.{versionNamespace}.{identifier}";
    }

    private static IReadOnlyDictionary<string, IReadOnlyList<string>> BuildParameterChoices(
        IReadOnlyList<ProtocolDefinition> definitions)
    {
        var choicesByParameter = new Dictionary<string, List<string>>(StringComparer.Ordinal);
        HashSet<string> envelopeParameters = definitions
            .SelectMany(static definition => definition.Parameters)
            .Where(static parameter => parameter.TypeNumber == 1023)
            .Select(static parameter => parameter.Name)
            .ToHashSet(StringComparer.Ordinal);
        foreach (ChoiceDefinition choice in definitions.SelectMany(static definition => definition.Choices))
        {
            foreach (string parameterName in choice.ParameterTypes)
            {
                if (envelopeParameters.Contains(parameterName))
                {
                    continue;
                }

                if (!choicesByParameter.TryGetValue(parameterName, out List<string>? choices))
                {
                    choices = [];
                    choicesByParameter.Add(parameterName, choices);
                }

                if (!choices.Contains(choice.Name, StringComparer.Ordinal))
                {
                    choices.Add(choice.Name);
                }
            }
        }

        return choicesByParameter.ToDictionary(
            static pair => pair.Key,
            static pair => (IReadOnlyList<string>)pair.Value.AsReadOnly(),
            StringComparer.Ordinal);
    }

    private IReadOnlySet<string> FindCanonicalTrailingEnvelopeChoices(
        IReadOnlyList<ProtocolDefinition> definitions)
    {
        var choices = new HashSet<string>(StringComparer.Ordinal);
        foreach (ProtocolDefinition definition in definitions)
        {
            IEnumerable<IReadOnlyList<ProtocolMemberDefinition>> owners = definition.Messages
                .Select(static owner => owner.Members)
                .Concat(definition.Parameters.Select(static owner => owner.Members))
                .Concat(definition.CustomMessages.Select(static owner => owner.Members))
                .Concat(definition.CustomParameters.Select(static owner => owner.Members));
            foreach (IReadOnlyList<ProtocolMemberDefinition> members in owners)
            {
                for (int index = 0; index < members.Count - 1; index++)
                {
                    if (members[index] is ChoiceReferenceDefinition choice
                        && members[index + 1] is ParameterReferenceDefinition parameter
                        && symbols.IsEnvelopeParameter(parameter.ParameterType)
                        && choiceDefinitions[choice.ChoiceType].ParameterTypes.Contains(
                            parameter.ParameterType,
                            StringComparer.Ordinal))
                    {
                        choices.Add(choice.ChoiceType);
                    }
                }
            }
        }

        return choices;
    }

    private static string EscapeXml(string value)
    {
        return value
            .Replace("&", "&amp;", StringComparison.Ordinal)
            .Replace("<", "&lt;", StringComparison.Ordinal)
            .Replace(">", "&gt;", StringComparison.Ordinal)
            .Replace("\"", "&quot;", StringComparison.Ordinal)
            .Replace("'", "&apos;", StringComparison.Ordinal)
            .Replace("\r", "&#xD;", StringComparison.Ordinal)
            .Replace("\n", "&#xA;", StringComparison.Ordinal)
            .Replace("\u0085", "&#x85;", StringComparison.Ordinal)
            .Replace("\u2028", "&#x2028;", StringComparison.Ordinal)
            .Replace("\u2029", "&#x2029;", StringComparison.Ordinal);
    }

    private static string FormatCardinality(Cardinality cardinality)
    {
        string maximum = cardinality.Maximum?.ToString(CultureInfo.InvariantCulture) ?? "N";
        return $"{cardinality.Minimum.ToString(CultureInfo.InvariantCulture)}..{maximum}";
    }

    private static string FormatInt64(long value)
    {
        return value == long.MinValue
            ? "global::System.Int64.MinValue"
            : $"{value.ToString(CultureInfo.InvariantCulture)}L";
    }

    private sealed record GeneratedProperty(
        string Type,
        string Name,
        string Description,
        ProtocolMemberDefinition? Definition);
}
